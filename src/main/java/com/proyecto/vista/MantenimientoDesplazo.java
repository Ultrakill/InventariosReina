    private void listar() {
        lista = desplazoControlador.buscarTodos();
        lista = ObservableCollections.observableList(lista);
        JTableBinding binding = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ, lista, tbldesplazo);

        BeanProperty bId = BeanProperty.create("id");
        BeanProperty bFecha = BeanProperty.create("fecha");
        BeanProperty bDetalle = BeanProperty.create("detalle");
        BeanProperty bBien = BeanProperty.create("bienId");
        BeanProperty bAmbienteOId = BeanProperty.create("ambienteOId");
        BeanProperty bAmbienteDId = BeanProperty.create("ambienteDId");

        binding.addColumnBinding(bId).setColumnName("ID").setEditable(false);

        binding.addColumnBinding(bFecha).setColumnName("FECHA").setEditable(false);
        binding.addColumnBinding(bDetalle).setColumnName("DETALLE").setEditable(false);
        binding.addColumnBinding(bBien).setColumnName("BIEN").setEditable(false);
        binding.addColumnBinding(bAmbienteOId).setColumnName("AMBIENTE_ORIGEN").setEditable(false);
        binding.addColumnBinding(bAmbienteDId).setColumnName("AMBIENTE_DESTINO").setEditable(false);

        binding.bind();

    }
