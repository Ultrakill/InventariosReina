    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        String palabra = "";
        String palabra2 = "";
        if (accion == 1) {
            palabra = "registrar";
            palabra2 = "registrado";

            if (JOptionPane.showConfirmDialog(null, "¿Desea " + palabra + " el Desplazo?", "Mensaje del Sistema", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                desplazoControlador.getSeleccionado().setFecha(Date.valueOf(fechaField.getText()));
                desplazoControlador.getSeleccionado().setDetalle(detalleField.getText());
                desplazoControlador.getSeleccionado().getBienId().setId(Integer.parseInt(bienIdField.getText()));
                desplazoControlador.getSeleccionado().getAmbienteorigenId().setId(Integer.parseInt(ambienteorigenIdField.getText()));
                desplazoControlador.getSeleccionado().getAmbientedestinoId().setId(Integer.parseInt(ambientedestinoIdField.getText()));

                desplazoControlador.accion(accion);
                lista.add(desplazoControlador.getSeleccionado());
                if (accion == 1) {
                    JOptionPane.showMessageDialog(null, "Desplazo " + palabra2 + " correctamente", "Mensaje del Sistema", JOptionPane.INFORMATION_MESSAGE);
                    fechaField.setText(null);
                    detalleField.setText(null);
                    bienIdField.setText(null);
                    ambienteorigenIdField.setText(null);
                    ambientedestinoIdField.setText(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Desplazo no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                fechaField.setText(null);
                detalleField.setText(null);
                bienIdField.setText(null);
                ambienteorigenIdField.setText(null);
                ambientedestinoIdField.setText(null);
                JOptionPane.showMessageDialog(null, "Desplazo no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            }
        } else if (accion == 2) {
            palabra = "modificar";
            palabra2 = "modificado";

            if (JOptionPane.showConfirmDialog(null, "¿Desea " + palabra + " el Ambiente?", "Mensaje del Sistema", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                if (accion == 2) {
                    JOptionPane.showMessageDialog(null, "Ambiente " + palabra2 + " correctamente", "Mensaje del Sistema", JOptionPane.INFORMATION_MESSAGE);
                    fechaField.setText(null);
                    detalleField.setText(null);
                    bienIdField.setText(null);
                    ambienteorigenIdField.setText(null);
                    ambientedestinoIdField.setText(null);
                } else {
                    JOptionPane.showMessageDialog(null, "Ambiente no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                fechaField.setText(null);
                detalleField.setText(null);
                bienIdField.setText(null);
                ambienteorigenIdField.setText(null);
                ambientedestinoIdField.setText(null);
                JOptionPane.showMessageDialog(null, "Ambiente no " + palabra2, "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
            }
        }

        btnguardar.setEnabled(false);
        fechaField.setEnabled(false);
        detalleField.setEnabled(false);;
        bienIdField.setEnabled(false);
        ambienteorigenIdField.setEnabled(false);
        ambientedestinoIdField.setEnabled(false);
        btnnuevo.setEnabled(true);
        btnmodificar.setEnabled(true);
    }//GEN-LAST:event_btnguardarActionPerformed

