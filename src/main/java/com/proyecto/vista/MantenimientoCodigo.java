    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        // TODO add your handling code here:
        accion = AbstractControlador.ELIMINAR;
        if (tblcodigo.getSelectedRow() != -1) {
            Integer codigos = (Integer) tblcodigo.getModel().getValueAt(tblcodigo.getSelectedRow(), 0);

            Codigo codigo = codigoControlador.buscarPorId(codigos);

            if (codigo != null) {
                if (JOptionPane.showConfirmDialog(null, "¬øDesea Eliminar el C√≥digo?", "Mensaje del Sistema", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {

                    int[] filas = tblcodigo.getSelectedRows();
                    for (int i = 0; i < filas.length; i++) {
                        Codigo ambiente2 = lista.get(filas[0]);
                        lista.remove(ambiente2);
                        codigoControlador.setSeleccionado(ambiente2);
                        codigoControlador.accion(accion);
                    }
                    if (codigoControlador.accion(accion) == 3) {
                        JOptionPane.showMessageDialog(null, "C√≥digo eliminado correctamente", "Mensaje del Sistema", JOptionPane.INFORMATION_MESSAGE);

                    } else {
                        JOptionPane.showMessageDialog(null, "C√≥digo no eliminado", "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "C√≥digo no eliminado", "Mensaje del Sistema", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btneliminarActionPerformed

