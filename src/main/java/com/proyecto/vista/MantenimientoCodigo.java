    private void listar() {
        lista = codigoControlador.buscarTodos();
        lista = ObservableCollections.observableList(lista);
        JTableBinding binding = SwingBindings.createJTableBinding(AutoBinding.UpdateStrategy.READ, lista, tblcodigo);

        BeanProperty bId = BeanProperty.create("id");
        BeanProperty bCodigo = BeanProperty.create("codigo");
        BeanProperty bSerie = BeanProperty.create("serie");

        binding.addColumnBinding(bId).setColumnName("ID").setEditable(false);

        binding.addColumnBinding(bCodigo).setColumnName("CODIGO").setEditable(false);
        binding.addColumnBinding(bSerie).setColumnName("SERIE").setEditable(false);

        binding.bind();

    }
